package com.zuoxiaolong.niubi.job.api.data;

/**
 * ZK数据节点抽象类
 *
 * @author Xiaolong Zuo
 * @since 0.9.3
 */
public abstract class AbstractData {

    protected String id;

    protected String path;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

}
